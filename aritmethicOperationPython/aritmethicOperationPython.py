#Arithmetic Operators
print('==============Arithmetic Operators =============')
firstNumber = 8
secondNumber = 3
#Addition
resultAddition =  firstNumber + secondNumber
#Subtraction
resultSubtraction = firstNumber - secondNumber
#Multiplication
resultMultiplication = firstNumber * secondNumber
#Division
resultDivision = firstNumber / secondNumber
#Floor Division
resultFloorDivision = firstNumber // secondNumber
#Modulus
resultModulus = firstNumber % secondNumber
#Exponent
resultExponent = firstNumber ** secondNumber

print('First Number:',firstNumber)
print('Second Number:',secondNumber)
print('==============Result =============')
print('Addition (+):',resultAddition)
print('Subtraction (-):',resultSubtraction)
print('Multiplication (*):',resultMultiplication)
print('Division (/):',resultDivision)
print('Floor Division (//):',resultFloorDivision)
print('Modulus (%):',resultModulus)
print('Exponent (**):',resultExponent)